class Verhoeff(ModBase):
    TESTS = [2363, 84736430954837284567892]
    def check_digit(self, number: list):
        check = 0
        for i, d in enumerate(reversed(number), 1):
            perm = PERM_TABLE[i % 8][d]
            check = MULT_TABLE[check][perm]

        return INV_TABLE[check]
